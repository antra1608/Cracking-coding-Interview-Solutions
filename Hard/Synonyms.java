package Hard;

public class Synonyms {

	
	Name a;
	Name b;
	public Synonyms(Name a,Name b)
	{
		this.a=a;
		this.b=b;
	}
	public Name getA() {
		return a;
	}
	public void setA(Name a) {
		this.a = a;
	}
	public Name getB() {
		return b;
	}
	public void setB(Name b) {
		this.b = b;
	}
}
