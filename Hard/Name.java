package Hard;

public class Name {

	String name;
	int freq;
	public Name(String name,int freq)
	{
		this.name=name;
		this.freq=freq;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getFreq() {
		return freq;
	}
	public void setFreq(int freq) {
		this.freq = freq;
	}
	
	
	
	
}
